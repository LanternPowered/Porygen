package org.lanternpowered.porygen.util

fun ChunkPos(x: Int, y: Int): ChunkPos = ChunkPos((x.toLong() shl 32) or y.toLong())

inline class ChunkPos(val packed: Long) {

    val x: Int get() = (this.packed shr 32).toInt()
    val y: Int get() = ((this.packed shl 32) shr 32).toInt()
}
